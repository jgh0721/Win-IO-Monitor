#define IOCTL_DEL_PROCESS_POLICY_BY_PID         CTL_CODE( WINIOISOLATION_DEVICE_TYPE, 0x800 + 22, METHOD_BUFFERED, FILE_ANY_ACCESS )
#define IOCTL_DEL_PROCESS_POLICY_BY_MASK        CTL_CODE( WINIOISOLATION_DEVICE_TYPE, 0x800 + 23, METHOD_BUFFERED, FILE_ANY_ACCESS )
#define IOCTL_GET_PROCESS_POLICY_COUNT          CTL_CODE( WINIOISOLATION_DEVICE_TYPE, 0x800 + 24, METHOD_BUFFERED, FILE_ANY_ACCESS )
#define IOCTL_GET_PROCESS_POLICY_ID_LIST        CTL_CODE( WINIOISOLATION_DEVICE_TYPE, 0x800 + 25, METHOD_BUFFERED, FILE_ANY_ACCESS )
#define IOCTL_GET_PROCESS_POLICY_BY_ID          CTL_CODE( WINIOISOLATION_DEVICE_TYPE, 0x800 + 26, METHOD_BUFFERED, FILE_ANY_ACCESS )
