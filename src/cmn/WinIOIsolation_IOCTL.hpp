#define WINIOISOLATION_DEVICE_TYPE 0x8227

#ifndef CTL_CODE

    //
    // Macro definition for defining IOCTL and FSCTL function control codes.  Note
    // that function codes 0-2047 are reserved for Microsoft Corporation, and
    // 2048-4095 are reserved for customers.
    //

    #define CTL_CODE( DeviceType, Function, Method, Access ) (                 \
        ((DeviceType) << 16) | ((Access) << 14) | ((Function) << 2) | (Method) \
    )

#endif

#ifndef METHOD_BUFFERED
#define METHOD_BUFFERED                 0
#endif

#ifndef FILE_ANY_ACCESS
#define FILE_ANY_ACCESS                 0
#endif

#define IOCTL_SET_DRIVER_CONFIG                 CTL_CODE( WINIOISOLATION_DEVICE_TYPE, 0x800 + 1, METHOD_BUFFERED, FILE_ANY_ACCESS )
#define IOCTL_GET_DRIVER_CONFIG                 CTL_CODE( WINIOISOLATION_DEVICE_TYPE, 0x800 + 2, METHOD_BUFFERED, FILE_ANY_ACCESS )

#define IOCTL_SET_DRIVER_STATUS                 CTL_CODE( WINIOISOLATION_DEVICE_TYPE, 0x800 + 3, METHOD_BUFFERED, FILE_ANY_ACCESS )
#define IOCTL_GET_DRIVER_STATUS                 CTL_CODE( WINIOISOLATION_DEVICE_TYPE, 0x800 + 4, METHOD_BUFFERED, FILE_ANY_ACCESS )

#define IOCTL_SET_STUB_CODE                     CTL_CODE( WINIOISOLATION_DEVICE_TYPE, 0x800 + 5, METHOD_BUFFERED, FILE_ANY_ACCESS )
#define IOCTL_GET_STUB_CODE                     CTL_CODE( WINIOISOLATION_DEVICE_TYPE, 0x800 + 6, METHOD_BUFFERED, FILE_ANY_ACCESS )

#define IOCTL_ADD_GLOBAL_POLICY                 CTL_CODE( WINIOISOLATION_DEVICE_TYPE, 0x800 + 10, METHOD_BUFFERED, FILE_ANY_ACCESS )
